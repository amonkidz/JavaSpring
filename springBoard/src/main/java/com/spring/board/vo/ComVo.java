package com.spring.board.vo;

public class ComVo {
	
	private String codeType;
	private String codeId;
	private String codeName;
	private String codeCreator;
	private String codeCreateTime;
	private String codeModifier;
	private String codeModifiedTime;
	
	public String getCodeType() {
		return codeType;
	}
	public void setCodeType(String codeType) {
		this.codeType = codeType;
	}
	public String getCodeId() {
		return codeId;
	}
	public void setCodeId(String codeId) {
		this.codeId = codeId;
	}
	public String getCodeName() {
		return codeName;
	}
	public void setCodeName(String codeName) {
		this.codeName = codeName;
	}
	public String getCodeCreator() {
		return codeCreator;
	}
	public void setCodeCreator(String codeCreator) {
		this.codeCreator = codeCreator;
	}
	public String getCodeCreateTime() {
		return codeCreateTime;
	}
	public void setCodeCreateTime(String codeCreateTime) {
		this.codeCreateTime = codeCreateTime;
	}
	public String getCodeModifier() {
		return codeModifier;
	}
	public void setCodeModifier(String codeModifier) {
		this.codeModifier = codeModifier;
	}
	public String getCodeModifiedTime() {
		return codeModifiedTime;
	}
	public void setCodeModifiedTime(String codeModifiedTime) {
		this.codeModifiedTime = codeModifiedTime;
	}

}